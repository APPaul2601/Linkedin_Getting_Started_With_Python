# Python gives floats from any divisions or any operations with a float in it

a_division = 20 / 4
print(a_division)

a_add = 4 + 4.0
print(a_add)

a_mul = 4 * 4.0
print(a_mul)

a_expo = 4 ** 4.0
print(a_expo)

# You can also change the result from float to int - called CASTING
# When you cast from a float to an int, python doesn't round the number for you
print(int(a_expo))

# To round you can use the round func
b = 14/5
print(round(b))

