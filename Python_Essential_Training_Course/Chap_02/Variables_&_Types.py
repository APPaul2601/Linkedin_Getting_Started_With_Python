# Variables:
# Rules : it can not start with a number like 1x = 2
# It can not have special char like ! @ # etc.

x = 5
name = "Ryan"
y = 1.5
test = True
number_complex = 2j

print(type(x))
print(type(name))
print(type(y))
print(type(test))
print(type(number_complex))

# You can also add strings together

added_strings = "string 1 " + "string 2"
print(added_strings)

added_string_nr = "1" + "1"
print(added_string_nr)
